func CreateAccesscontextmanager_accesspolicies_serviceperimeters_commitTool(cfg *config.APIConfig) models.Tool {
	tool := mcp.NewTool("post_v1_parent_servicePerimeters_commit",
		mcp.WithDescription("Commits the dry-run specification for all the service perimeters in an access policy. A commit operation on a service perimeter involves copying its `spec` field to the `status` field of the service perimeter. Only service perimeters with `use_explicit_dry_run_spec` field set to true are affected by a commit operation. The long-running operation from this RPC has a successful status after the dry-run specifications for all the service perimeters have been committed. If a commit fails, it causes the long-running operation to return an error response and the entire commit operation is cancelled. When successful, the Operation.response field contains CommitServicePerimetersResponse. The `dry_run` and the `spec` fields are cleared after a successful commit operation."),
		mcp.WithString("parent", mcp.Required(), mcp.Description("Required. Resource name for the parent Access Policy which owns all Service Perimeters in scope for the commit operation. Format: `accessPolicies/{policy_id}`")),
		mcp.WithString("etag", mcp.Description("Input parameter: Optional. The etag for the version of the Access Policy that this commit operation is to be performed on. If, at the time of commit, the etag for the Access Policy stored in Access Context Manager is different from the specified etag, then the commit operation will not be performed and the call will fail. This field is not required. If etag is not provided, the operation will be performed as if a valid etag is provided.")),
	)

	return models.Tool{
		Definition: tool,
		Handler:    Accesscontextmanager_accesspolicies_serviceperimeters_commitHandler(cfg),
	}
}
