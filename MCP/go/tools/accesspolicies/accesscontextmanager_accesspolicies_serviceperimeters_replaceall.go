func CreateAccesscontextmanager_accesspolicies_serviceperimeters_replaceallTool(cfg *config.APIConfig) models.Tool {
	tool := mcp.NewTool("post_v1_parent_servicePerimeters_replaceAll",
		mcp.WithDescription("Replace all existing service perimeters in an access policy with the service perimeters provided. This is done atomically. The long-running operation from this RPC has a successful status after all replacements propagate to long-lasting storage. Replacements containing errors result in an error response for the first error encountered. Upon an error, replacement are cancelled and existing service perimeters are not affected. The Operation.response field contains ReplaceServicePerimetersResponse."),
		mcp.WithString("parent", mcp.Required(), mcp.Description("Required. Resource name for the access policy which owns these Service Perimeters. Format: `accessPolicies/{policy_id}`")),
		mcp.WithString("etag", mcp.Description("Input parameter: Optional. The etag for the version of the Access Policy that this replace operation is to be performed on. If, at the time of replace, the etag for the Access Policy stored in Access Context Manager is different from the specified etag, then the replace operation will not be performed and the call will fail. This field is not required. If etag is not provided, the operation will be performed as if a valid etag is provided.")),
		mcp.WithArray("servicePerimeters", mcp.Description("Input parameter: Required. The desired Service Perimeters that should replace all existing Service Perimeters in the Access Policy.")),
	)

	return models.Tool{
		Definition: tool,
		Handler:    Accesscontextmanager_accesspolicies_serviceperimeters_replaceallHandler(cfg),
	}
}
